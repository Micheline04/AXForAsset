package com.example.labux;

public class ItemList {
    String item_name;
    int item_desc;
    int item_detail_desc;
    int image;

    public ItemList(String item_name, int item_desc, int item_detail_desc, int image) {
        this.item_name = item_name;
        this.item_desc = item_desc;
        this.item_detail_desc = item_detail_desc;
        this.image = image;
    }

}
